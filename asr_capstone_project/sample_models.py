def final_model(input_dim, filters, kernel_size, conv_stride,
    conv_border_mode, units, nb_head, size_per_head, output_dim=29):
    """ Build a deep network for speech 
    """
    # Main acoustic input
    input_data = Input(name='the_input', shape=(None, input_dim))
    # TODO: Specify the layers in your network
    # Add convolutional layer
    conv_1d = Conv1D(filters, kernel_size, 
                     strides=conv_stride, 
                     padding=conv_border_mode,
                     activation='relu',
                     name='conv1d')(input_data)
    # Add batch normalization
    bn_cnn = BatchNormalization(name='bn_conv_1d')(conv_1d)
    # Add a multi head self-attention layer
    mhsa = Attention(nb_head, size_per_head, name='mhsa')([bn_cnn, bn_cnn, bn_cnn])
    # Add batch normalization
    bn_mhsa = BatchNormalization(name='bn_mhsa')(mhsa)
    context = concatenate([bn_cnn, bn_mhsa], axis=-1, name='context')
    # Add a bidirectional recurrent layer
    bidir_rnn = Bidirectional(GRU(units, activation='relu',
            return_sequences=True, implementation=2), merge_mode='concat', name='bidir_rnn')(context)
    # Add batch normalization
    bn_bidir_rnn = BatchNormalization(name='bn_bidir_rnn')(bidir_rnn)
    # Add a TimeDistributed(Dense(output_dim)) layer
    time_dense = TimeDistributed(Dense(output_dim), name='dense')(bn_bidir_rnn) 
    # Add softmax activation layer
    y_pred = Activation('softmax', name='softmax')(time_dense)
    # Specify the model
    model = Model(inputs=input_data, outputs=y_pred)
    model.output_length = lambda x: cnn_output_length(
        x, kernel_size, conv_border_mode, conv_stride)
    print(model.summary())
    return model